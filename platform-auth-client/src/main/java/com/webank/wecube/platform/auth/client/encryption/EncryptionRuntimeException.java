package com.webank.wecube.platform.auth.client.encryption;

public class EncryptionRuntimeException extends RuntimeException {

    /**
     * 
     */
    private static final long serialVersionUID = 115523361878991641L;

    public EncryptionRuntimeException() {
        super();
    }

    public EncryptionRuntimeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public EncryptionRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }

    public EncryptionRuntimeException(String message) {
        super(message);
    }

    public EncryptionRuntimeException(Throwable cause) {
        super(cause);
    }
    
    

}
