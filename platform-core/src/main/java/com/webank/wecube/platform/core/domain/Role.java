package com.webank.wecube.platform.core.domain;

public class Role {
    public final static String ADMIN = "ADMIN";
    public final static String REGULAR_USER = "REGULAR_USER";
    public final static String READ_ONLY_USER = "READ_ONLY_USER";

    private Integer roleId;
    private String roleName;
    private String roleType;
    private String description;

    public Role(Integer roleId, String roleName, String roleType, String description) {
        super();
        this.roleId = roleId;
        this.roleName = roleName;
        this.roleType = roleType;
        this.description = description;
    }

    public Role() {
        super();
    }

    public Integer getRoleId() {
        return roleId;
    }

    public void setRoleId(Integer roleId) {
        this.roleId = roleId;
    }

    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }

    public String getRoleType() {
        return roleType;
    }

    public void setRoleType(String roleType) {
        this.roleType = roleType;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

}
